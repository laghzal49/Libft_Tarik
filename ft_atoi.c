#include "libft.h"

int	ft_atoi(const char *n)
{
	int	i;
	int	sign;
	int	j;

	i = 0;
	sign = 1;
	j = 0;
	while (n[j] && (n[j] == ' ' || (n[j] >= '\t' && n[j] <= '\r')))
		j++;
	if (n[j] == '-' || n[j] == '+')
	{
		if (n[j] == '-')
			sign = -1;
		j++;
	}
	while (n[j] && (n[j] >= '0' && n[j] <= '9'))
	{
		i = i * 10 + (n[j] - '0');
		j++;
	}
	return (i * sign);
}
